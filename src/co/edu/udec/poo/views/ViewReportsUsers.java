    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Collection<User> datos = User.usersDB.values();
        List<User> usersList = new ArrayList(datos);
        
        String columns[] = {"CEDULA", "NOMBRE", "EMAIL", "TELEFONO"};
        int totalUsuarios = usersList.size();
        String rows[][] = new String[totalUsuarios][columns.length];
        
        for(int i = 0; i  < usersList.size(); i++) {
            User user = usersList.get(i);
            rows[i][0] = user.getIdentificacion();
            rows[i][1] = user.getName();
            rows[i][2] = user.getEmail();
            rows[i][3] = user.getPhone();
        }
        
        DefaultTableModel dataTable = new DefaultTableModel();
        dataTable.setDataVector(rows, columns);
        TableUsers.setModel(dataTable);
        
        txtTotal.setText(totalUsuarios + "");
    }//GEN-LAST:event_formWindowOpened

