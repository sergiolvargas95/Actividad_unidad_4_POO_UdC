    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Collection<Book> datos = Book.booksDB.values();
        List<Book> booksList = new ArrayList(datos);
        
        String columns[] = {"CODIGO", "TITULO", "AUTOR", "P√ÅGINAS"};
        int totalUsuarios = booksList.size();
        String rows[][] = new String[totalUsuarios][columns.length];
        
        for(int i = 0; i  < booksList.size(); i++) {
            Book book = booksList.get(i);
            rows[i][0] = book.getCode();
            rows[i][1] = book.getTitle();
            rows[i][2] = book.getAuthor();
            rows[i][3] = String.valueOf(book.getPages());
        }
        
        DefaultTableModel dataTable = new DefaultTableModel();
        dataTable.setDataVector(rows, columns);
        TableBooks.setModel(dataTable);
        
        txtTotal.setText(totalUsuarios + "");
    }//GEN-LAST:event_formWindowOpened

